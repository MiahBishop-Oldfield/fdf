#include "libft.h"

size_t	ft_strlen(const char *s)
{
	size_t	i;

	i = 0;
	while (s[i])
		i++;
	return (i);
}
/* size_t is unsigned, so it can't be less than 0.
** it is returned here because the length of a string has to be at least 0. */